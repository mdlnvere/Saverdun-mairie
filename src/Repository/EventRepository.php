    public function findByDate(string $date): array
    {
        $query = $this->createQueryBuilder('e')
        ->where('e.dateStart = :date')
        ->orWhere('e.dateStart > :date')
        ->setParameter('date', $date);

        $q = $query->getQuery();
        return $q->execute();
    }
